// Function to map the language name to hl code
export function mapLanguageNameToHlCode(languageName: string): string {
  const hlCode = languageNameToHlCode[languageName.trim()];
  if (hlCode) {
    return hlCode;
  } else {
    console.warn(`Unsupported language: ${languageName}, defaulting to 'en'`);
    return "en";
  }
}
